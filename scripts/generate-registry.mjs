#!/usr/bin/env node
// generate-registry.mjs — Merges config/projects.json + mc-enrichment.json → src/generated/registry.ts

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs';
import { fileURLToPath } from 'url';
import path from 'path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../../..'); // workspace root
const MC = path.resolve(__dirname, '..');

// --- Read inputs ---
const registryPath = path.join(ROOT, 'config/projects.json');
if (!existsSync(registryPath)) {
  // On Vercel or CI where workspace root is unavailable, rely on committed registry.ts
  const outputPath = path.join(MC, 'src/generated/registry.ts');
  if (existsSync(outputPath)) {
    console.log('Skipped — config/projects.json not found, using committed registry.ts');
    process.exit(0);
  }
  console.error('ERROR: config/projects.json not found and no committed registry.ts exists');
  process.exit(1);
}
const registry = JSON.parse(readFileSync(registryPath, 'utf-8'));
const enrichment = JSON.parse(readFileSync(path.join(MC, 'src/data/mc-enrichment.json'), 'utf-8'));

// --- Helpers ---

function inferCategory(deploy) {
  if (!deploy || deploy === '—') return 'dev';
  const d = deploy.toLowerCase();
  if (d.includes('vercel') || d.includes('gh pages')) return 'running';
  if (d.includes('chrome')) return 'tool';
  return 'dev';
}

function inferPlatform(runtime) {
  if (!runtime) return undefined;
  const r = runtime.replace(/\*/g, '').trim().toLowerCase();
  if (r === 'wsl only' || r === 'wsl') return 'wsl';
  if (r.includes('wsl') && r.includes('win')) return 'both';
  return 'windows';
}

// --- Build PROJECTS + SERVICE_REGISTRY ---

const projects = [];
const services = [];

for (const [key, entry] of Object.entries(registry)) {
  if (entry.external) continue;

  const enrich = enrichment.projects[key] || {};
  const primaryRow = entry.rows[0];

  // ProjectConfig
  const project = {
    name: entry.name,
    folder: key,
    repo: enrich.repo || key,
    description: enrich.description || entry.name,
    techStack: entry.stack ? entry.stack.split(' + ') : [],
    category: enrich.categoryOverride || inferCategory(entry.deploy),
  };

  if (enrich.deployUrl) project.deployUrl = enrich.deployUrl;
  if (enrich.connections) project.connections = enrich.connections;

  const platform = inferPlatform(primaryRow.runtime);
  if (platform) project.platform = platform;

  if (enrich.runCmd) project.runCmd = enrich.runCmd;
  if (primaryRow.port) project.port = primaryRow.port;
  if (enrich.completionPct != null) project.completionPct = enrich.completionPct;
  if (enrich.priority) project.priority = enrich.priority;

  projects.push(project);

  // SERVICE_REGISTRY entries from rows
  for (const row of entry.rows) {
    if (!row.pm2) continue;

    if (row.pm2 === '(systemd)') {
      const override = enrichment.serviceOverrides?.[key] || {};
      services.push({
        name: key,
        runtime: 'wsl-systemd',
        category: override.category || 'wsl',
        projectFolder: key,
        ...(row.port ? { port: row.port } : {}),
        autorestart: override.autorestart ?? true,
        protected: override.protected ?? false,
      });
    } else {
      const svcName = row.pm2;
      const override = enrichment.serviceOverrides?.[svcName] || {};
      services.push({
        name: svcName,
        runtime: 'pm2',
        category: override.category || 'dev-server',
        projectFolder: key,
        ...(row.port ? { port: row.port } : {}),
        autorestart: override.autorestart ?? false,
        protected: override.protected ?? false,
      });
    }
  }
}

// Extra services from enrichment
if (enrichment.extraServices) {
  for (const extra of enrichment.extraServices) {
    services.push({
      autorestart: false,
      protected: false,
      runtime: 'pm2',
      ...extra,
    });
  }
}

// --- Generate TypeScript ---

const output = `// AUTO-GENERATED by scripts/generate-registry.mjs — do not edit manually
// Source: config/projects.json + src/data/mc-enrichment.json
// Re-generate: node scripts/generate-registry.mjs

import type { ProjectConfig, ServiceRegistryEntry } from '@/types';

export const PROJECTS: ProjectConfig[] = ${JSON.stringify(projects, null, 2)};

export const SERVICE_REGISTRY: ServiceRegistryEntry[] = ${JSON.stringify(services, null, 2)};
`;

// Write output
const outputPath = path.join(MC, 'src/generated/registry.ts');
mkdirSync(path.dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output, 'utf-8');

console.log('Generated', path.relative(MC, outputPath));
console.log(' ', projects.length, 'projects,', services.length, 'services');
